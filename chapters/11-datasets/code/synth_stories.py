"""
Synthetic data: generate simple stories from templates.
=======================================================
Real TinyStories (Eldan & Li, 2023) was generated by prompting GPT-3.5/4 to write short stories
using only words a 3-4 year old knows. The insight: if the *data* is simple enough, a *tiny* model
can learn to write coherent English. We can't call an LLM here, so we generate our own toy version
from templates — same idea, no network: a small, simple, repetitive corpus a small model can master.

Run:  python synth_stories.py          # prints a few sample stories
"""
import random

NAMES = ["Lily", "Tom", "Max", "Mia", "Ben", "Zoe", "Sam", "Ava", "Leo", "Ivy"]
ANIMALS = ["cat", "dog", "frog", "bird", "bear", "duck", "fox", "bunny"]
ADJ = ["little", "happy", "small", "brave", "shy", "kind", "silly"]
PLACES = ["park", "forest", "garden", "pond", "house", "hill"]
OBJECTS = ["ball", "box", "hat", "key", "leaf", "shell", "star"]
VERBS = ["play", "run", "jump", "sing", "dance", "hop"]
EMOTIONS = ["happy", "excited", "proud", "glad"]


def make_story(rng):
    """One short story: intro, a couple of body lines, a warm ending — all from small word lists."""
    name, animal, adj = rng.choice(NAMES), rng.choice(ANIMALS), rng.choice(ADJ)
    place, obj, verb, emo = rng.choice(PLACES), rng.choice(OBJECTS), rng.choice(VERBS), rng.choice(EMOTIONS)
    return (
        f"Once upon a time, there was a {adj} {animal} named {name}. "
        f"{name} loved to {verb} in the {place}. "
        f"One day, {name} found a {obj}. "
        f"{name} was very {emo}. "
        f"{name} and the {obj} played all day. The end.\n"
    )


def generate(n_stories, seed=0):
    """Return `n_stories` stories concatenated into one text corpus (deterministic given `seed`)."""
    rng = random.Random(seed)
    return "".join(make_story(rng) for _ in range(n_stories))


if __name__ == "__main__":
    text = generate(3, seed=0)
    print("Three sample synthetic stories:\n")
    print(text)
    big = generate(2000, seed=0)
    print(f"A corpus of 2000 stories is {len(big):,} characters "
          f"(~{len(big) / 2000:.0f} chars/story), built from tiny word lists — "
          f"simple and repetitive by design.")
